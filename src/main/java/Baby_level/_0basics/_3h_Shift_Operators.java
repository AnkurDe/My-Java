package Baby_level._0basics;

// Java Program to show the use of
// shift operators
public class _3h_Shift_Operators
{
    public static void main(String[] args)
    {
        int a = 10;

        // Using left shift
        System.out.println("a<<1 : " + (a << 1));

        // Using right shift
        System.out.println("a>>1 : " + (a >> 1));
    }
}
